<?php
namespace Core\Point\Domain;

class Point
{
    private $x;
    private $y;

    public function __construct($x, $y)
    {
        $this->x = $x;
        $this->y = $y;
    }

    /**
     * @return mixed
     */
    public function getX()
    {
        return $this->x;
    }

    /**
     * @param mixed $x
     */
    public function setX($x): void
    {
        $this->x = $x;
    }

    /**
     * @return mixed
     */
    public function getY()
    {
        return $this->y;
    }

    /**
     * @param mixed $y
     */
    public function setY($y): void
    {
        $this->y = $y;
    }

    #Logic
    public function distance(Point $pointCompare): ?float
    {
        return sqrt(pow($pointCompare->getX() - $this->getX(), 2) + pow($pointCompare->getY() - $this->getY(), 2));
    }
}
